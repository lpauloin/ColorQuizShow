// Host-side UI. Comments are in English for GitHub readability.
(function () {
  const app = document.getElementById("host-app");
  const stage = document.getElementById("host-stage");
  const token = app.dataset.hostToken;
  const playUrl = app.dataset.playUrl;
  const wsProtocol = window.location.protocol === "https:" ? "wss" : "ws";
  const socket = new WebSocket(`${wsProtocol}://${window.location.host}/ws/host/${token}/`);
  const joinEvents = [];
  let currentState = null;
  let progressTimer = null;
  let rankingPage = 0;

  const startBtn = document.getElementById("start-btn");
  const pauseBtn = document.getElementById("pause-btn");
  const resumeBtn = document.getElementById("resume-btn");

  function send(action) {
    socket.send(JSON.stringify({ action }));
  }

  startBtn.onclick = () => send("start");
  pauseBtn.onclick = () => send("pause");
  resumeBtn.onclick = () => send("resume");
  pauseBtn.style.display = "none";
  resumeBtn.style.display = "none";

  function updateControls(state, paused) {
    const waiting = !state || state.quiz.status === "waiting";
    const running = state && state.quiz.status === "running";
    startBtn.style.display = waiting ? "" : "none";
    pauseBtn.style.display = (running && !paused) ? "" : "none";
    resumeBtn.style.display = (running && paused) ? "" : "none";
  }

  function escapeHtml(value) {
    return String(value || "").replace(/[&<>'"]/g, c => ({"&":"&amp;","<":"&lt;",">":"&gt;","'":"&#39;","\"":"&quot;"}[c]));
  }

  function qrUrl() {
    // No Python QR dependency: the image is generated by a public QR image endpoint.
    return `https://api.qrserver.com/v1/create-qr-code/?size=360x360&data=${encodeURIComponent(playUrl)}`;
  }

  function progressHtml(labelFr, labelVi) {
    return `<div class="progress-wrap" title="${labelFr} / ${labelVi}"><div id="progress-bar" class="progress-bar"></div></div>`;
  }

  function startProgress(state) {
    clearInterval(progressTimer);
    const bar = document.getElementById("progress-bar");
    if (!bar || !state.phase_duration_ms) return;
    const started = Date.now() - state.elapsed_ms;
    progressTimer = setInterval(() => {
      const elapsed = Math.max(0, Date.now() - started);
      const ratio = Math.max(0, 1 - elapsed / state.phase_duration_ms);
      bar.style.width = `${ratio * 100}%`;
    }, 120);
  }

  function colorName(state, color) {
    const names = state.color_names[color];
    return `${names.fr} / ${names.vi}`;
  }

  function bilingualText(fr, vi) {
    if (fr.trim() === vi.trim()) return `<div>${escapeHtml(fr)}</div>`;
    return `<div>${escapeHtml(fr)}</div><div class="vi">${escapeHtml(vi)}</div>`;
  }

  function medalHtml(player) {
    const medal = player.medal || {};
    if (!medal.type) return "";
    const emoji = medal.type === "gold" ? "🥇" : medal.type === "silver" ? "🥈" : "🥉";
    return `<span class="trophy ${medal.type}" title="${escapeHtml(medal.fr)} / ${escapeHtml(medal.vi)}">${emoji}</span>`;
  }

  function averageTime(player) {
    if (!player.answered_count) return "-";
    return `${(player.avg_reaction_time_ms / 1000).toFixed(1)}s`;
  }

  function renderWaiting(state) {
    stage.className = "host-stage waiting-stage";
    stage.innerHTML = `
      <h1>${escapeHtml(state.quiz.title)}</h1>
      <h2>Scannez le QR code<br>Quét mã QR</h2>
      <img class="qr" src="${qrUrl()}" alt="QR code">
      <div class="join-url">${escapeHtml(playUrl)}</div>
      <h2>${state.participants_count} participants connectés<br>${state.participants_count} người chơi đã kết nối</h2>
      <div class="join-events">${joinEvents.slice(-3).map(name => `✅ ${escapeHtml(name)}`).join("<br>")}</div>
    `;
  }

  function renderQuestionIntro(state) {
    const q = state.question;
    stage.className = "host-stage full-screen-question";
    stage.style.backgroundImage = q.image_url ? `url('${q.image_url}')` : "";
    stage.innerHTML = `
      <div>
        <h1 class="question-title">Question ${state.question_index} / ${state.question_count}</h1>
        <div class="bilingual-text">
          ${bilingualText(q.text_fr, q.text_vi)}
        </div>
      </div>
      ${progressHtml("Lecture de la question", "Đọc câu hỏi")}
    `;
    startProgress(state);
  }

  function renderColorIntro(state, color) {
    const q = state.question;
    const answer = q.colors.find(item => item.color === color);
    stage.className = `host-stage color-intro ${color}`;
    stage.style.backgroundImage = "";
    stage.innerHTML = `
      <div>
        <div class="bilingual-text">
          ${bilingualText(answer.text_fr, answer.text_vi)}
        </div>
      </div>
      ${progressHtml("Lecture de la réponse", "Đọc câu trả lời")}
    `;
    startProgress(state);
  }

  function renderAnswering(state) {
    const q = state.question;
    stage.className = "host-stage answer-grid-stage";
    stage.style.backgroundImage = q.image_url ? `url('${q.image_url}')` : "";
    stage.innerHTML = `
      <div class="answer-grid-title">
        ${escapeHtml(q.text_fr)}<br>${escapeHtml(q.text_vi)}
      </div>
      <div class="answer-grid">
        ${q.colors.map(answer => `
          <article class="answer-card ${answer.color}">
            ${bilingualText(answer.text_fr, answer.text_vi)}
          </article>
        `).join("")}
      </div>
      ${progressHtml("Temps restant", "Thời gian còn lại")}
    `;
    startProgress(state);
  }

  function topAnswerIcon(rank) {
    if (rank === 1) return `<span class="q-top5-icon trophy gold">🏆</span>`;
    if (rank === 2) return `<span class="q-top5-icon trophy silver">🥈</span>`;
    if (rank === 3) return `<span class="q-top5-icon trophy bronze">🥉</span>`;
    return `<span class="q-top5-icon" style="color:#888">#${rank}</span>`;
  }

  function topAnswersHtml(topAnswers) {
    if (!topAnswers || !topAnswers.length) return "";
    return `
      <div class="q-top5">
        <div class="q-top5-title">Top réponses<br>Top câu trả lời</div>
        <div class="q-top5-list">
          ${topAnswers.map(p => `
            <div class="q-top5-row rank-${p.rank}">
              <div>${topAnswerIcon(p.rank)}</div>
              <div class="q-top5-name">${escapeHtml(p.name)}</div>
              <div class="q-top5-time">${(p.response_time_ms / 1000).toFixed(2)}s</div>
            </div>
          `).join("")}
        </div>
      </div>
    `;
  }

  function renderResult(state) {
    const color = state.question.correct_color;
    const topAnswers = state.question_top_answers || [];
    stage.className = `host-stage result-stage`;
    stage.style.backgroundImage = "";
    const answer = state.question.colors.find(c => c.color === color);
    const correctBlock = `
      <div class="result-answer">
        <h1>Bonne réponse<br>Đáp án đúng</h1>
        <div class="color-intro ${color}" style="border-radius:32px;">
          <div class="bilingual-text">
            ${bilingualText(answer.text_fr, answer.text_vi)}
          </div>
        </div>
      </div>
    `;
    if (topAnswers.length) {
      stage.innerHTML = `
        <div class="result-layout">
          ${correctBlock}
          ${topAnswersHtml(topAnswers)}
        </div>
        ${progressHtml("Résultat", "Kết quả")}
      `;
    } else {
      stage.innerHTML = `${correctBlock}${progressHtml("Résultat", "Kết quả")}`;
    }
    startProgress(state);
  }

  function podiumHtml(rankings) {
    const top = rankings.slice(0, 5);
    const maxCorrect = Math.max(1, ...top.map(p => p.correct_count));
    return `<div class="podium">${top.map(p => `
      <div class="podium-card">
        <div class="podium-rank">${medalHtml(p)} #${p.rank}</div>
        <div>${escapeHtml(p.name)}</div>
        <div>${p.correct_count} bonnes réponses<br>${p.correct_count} câu đúng</div>
        <div>${averageTime(p)} moyen<br>trung bình</div>
        <div class="bar" style="width:${Math.max(8, p.correct_count / maxCorrect * 100)}%"></div>
      </div>
    `).join("")}</div>`;
  }

  function renderFinished(state) {
    stage.className = "host-stage ranking-stage";
    stage.style.backgroundImage = "";
    const rankings = state.rankings || [];
    if (!rankings.length) {
      stage.innerHTML = `<h1>Podium final<br>Bảng xếp hạng chung cuộc</h1><p>Aucun participant / Không có người chơi</p>`;
      return;
    }
    const pages = Math.max(1, Math.ceil(rankings.length / 10));
    const showPodium = rankingPage % (pages + 1) === 0;
    if (showPodium) {
      stage.innerHTML = `<h1>🥇 Podium final<br>🥇 Bảng xếp hạng chung cuộc</h1>${podiumHtml(rankings)}`;
    } else {
      const pageIndex = (rankingPage % (pages + 1)) - 1;
      const rows = rankings.slice(pageIndex * 10, pageIndex * 10 + 10);
      stage.innerHTML = `
        <h1>Classement général<br>Bảng xếp hạng tổng</h1>
        <table class="ranking-table">
          <thead><tr><th>#</th><th>Nom<br>Tên</th><th>Bonnes réponses<br>Câu đúng</th><th>Temps moyen<br>Thời gian TB</th></tr></thead>
          <tbody>${rows.map(p => `
            <tr><td>${medalHtml(p)} ${p.rank}</td><td>${escapeHtml(p.name)}</td><td>${p.correct_count}</td><td>${averageTime(p)}</td></tr>
          `).join("")}</tbody>
        </table>
      `;
    }
    setTimeout(() => {
      if (currentState && currentState.quiz.status === "finished") {
        rankingPage += 1;
        renderFinished(currentState);
      }
    }, 7000);
  }

  function renderState(state) {
    currentState = state;
    if (state.quiz.status === "finished") return renderFinished(state);
    if (state.phase === "waiting_players") return renderWaiting(state);
    if (state.phase === "intro_question") return renderQuestionIntro(state);
    if (state.phase.startsWith("intro_")) return renderColorIntro(state, state.phase.replace("intro_", ""));
    if (state.phase === "answering") return renderAnswering(state);
    if (state.phase === "result") return renderResult(state);
  }

  socket.onmessage = event => {
    const message = JSON.parse(event.data);
    if (message.type === "state") {
      if (message.paused) {
        currentState = message.state;
        clearInterval(progressTimer);
      } else {
        renderState(message.state);
      }
      updateControls(message.state, message.paused);
    }
    if (message.type === "player_joined" && (!currentState || currentState.phase === "waiting_players")) {
      joinEvents.push(`${message.name} vient de se connecter / vừa tham gia`);
      if (currentState) currentState.participants_count = message.total_players;
      if (currentState) renderWaiting(currentState);
    }
  };
})();
